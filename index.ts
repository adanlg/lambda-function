const web3 = new Web3(new Web3.providers.HttpProvider(process.env.RPC_URL as string));
let privateKey = process.env.PRIVATE_KEY as string;

// Ensure the private key is formatted correctly
privateKey = privateKey.startsWith("0x") ? privateKey : `0x${privateKey}`;

if (!privateKey.match(/^0x[0-9a-fA-F]{64}$/)) {
  throw new Error('Invalid or missing private key');
}

const mintTokens = async (toAddress: string, amount: string) => {
  console.log(`Minting tokens for address: ${toAddress}, amount: ${amount}`);

  const contract = new web3.eth.Contract(contractABI, contractAddress);
  const account = web3.eth.accounts.privateKeyToAccount(privateKey);
  web3.eth.accounts.wallet.add(account);

  console.log(`Account address: ${account.address}`);

  const data = contract.methods.mint(toAddress, web3.utils.toWei(amount, 'ether')).encodeABI();

  const tx = {
    from: account.address,
    to: contractAddress,
    gas: 2000000,
    maxPriorityFeePerGas: web3.utils.toWei('2', 'gwei'),  // Max priority fee per gas (tip for miners)
    maxFeePerGas: web3.utils.toWei('100', 'gwei'),       // Max fee per gas (base fee + tip)
    data,
  };
  

  console.log('Transaction data:', tx);

  const signedTx = await web3.eth.accounts.signTransaction(tx, privateKey);
  console.log('Signed transaction:', signedTx);

  return web3.eth.sendSignedTransaction(signedTx.rawTransaction as string);
};


export const handler = async (event: APIGatewayEvent): Promise<APIGatewayProxyResult> => {
  try {
    console.log('Received event:', event);
    
    const body = event.body ? JSON.parse(event.body) : {};
    const { toAddress, amount } = body;

    console.log('Parsed body:', body);

    if (!toAddress || !amount) {
      console.log('Missing parameters');
      return {
        statusCode: 400,
        body: JSON.stringify({ message: 'Missing parameters' }),
      };
    }

    const receipt = await mintTokens(toAddress, amount);
    console.log('Transaction receipt:', receipt);

    // Convert BigInt values to strings for JSON serialization
    const safeReceipt = JSON.stringify(receipt, (key, value) =>
      typeof value === 'bigint' ? value.toString() : value
    );

    return {
      statusCode: 200,
      body: JSON.stringify({ message: 'Tokens minted successfully', receipt: safeReceipt }),
    };
  } catch (error) {
    console.error('Error minting tokens:', error);

    const err = error as Error;

    return {
      statusCode: 500,
      body: JSON.stringify({ message: 'Error minting tokens', error: err.message }),
    };
  }
};
